
public class num1to10 {

	public static void main(String[] args) {
		// Write a program to print numbers from 1 to 10.
		
		// FOR
		for(int i = 1; i <= 10; i++) {
			System.out.println(i);
		}
		
		// WHILE
		int i = 1;
		
		while(i <= 10) {
			System.out.println(i);
			i++;
		}
		
		// DO WHILE
		int k = 1;
		
		do {
			System.out.println(k);
			k++;
		}while(k <= 10);

	}

}
